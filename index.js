// language bar chart code goes here  
let height = 500;
let width = 2400;
let margin = 50;

let frame = d3.select("#langchart")
        .append("svg")
        .attr("width", width)
        .attr("height", height+500); 

let linearScale = d3.scaleLinear()
        .domain([20, 1600])
        .range([height-margin, margin]);

frame.append("g")
        .attr("transform", `translate(${margin}, 0)`)
        .call(d3.axisLeft(linearScale));

let bandScale = d3.scaleBand()
        .domain(myData.map(function(d) { return d.language; }))
        .range([margin, width-margin])
        .padding(0.2);

frame.append("g")
        .attr("transform", `translate(0, ${height-margin})`)
        .call(d3.axisBottom(bandScale))
        .selectAll("text")
        .attr("transform", "rotate(10)")
        .attr("text-anchor", "start");

frame.append("text")
        .attr("transform", "rotate(-90)")
        .attr("x", -(height / 2))
        .attr("y", 12)
        .attr("text-anchor", "middle")
        .text("Total Speakers (Millions)");        

frame.append("text")
        .attr("x", width / 2)
        .attr("y", height + margin - 10)
        .attr("text-anchor", "middle")
        .text("Language");        

frame.append("text")
        .attr("x", width / 2)
        .attr("y", margin / 2)
        .attr("text-anchor", "middle")
        .style("font-size", "16px")
        .text("Most Spoken Languages in the World");        

let selected = null;       

frame.selectAll("rect")
        .data(myData)
        .join("rect")
        .attr("x", function(d, i){
          return bandScale(d.language);
        })
        .attr("y", function(d, i){
          return (linearScale(d.total_millions));
        })
        .attr("width", bandScale.bandwidth())
        .attr("height", function(d,i){
          return ((height-margin) - linearScale(d.total_millions));
        })
        .attr("fill", "black")
        .on("click", function(event, d) {
          frame.selectAll("rect").attr("fill", "black");
          if (selected === d) {
            selected = null;
          } 
          else {
            d3.select(this).attr("fill", "steelblue");
            selected = d;
          }
        });

// language pie chart code goes here
let height2 = 500;
let width2 = 500;
let radius = Math.min(width2, height2) / 2 - margin; 

let familyData = d3.rollup(myData, v => d3.sum(v, d => +d.total_millions), d => d.family);

familyData = Array.from(familyData, ([family, total_millions]) => ({ family, total_millions }));

let pieFrame = d3.select("#langpie")
        .append("svg")
        .attr("width", width2)
        .attr("height", height2)
        .append("g")
        .attr("transform", `translate(${width2 / 2}, ${height2 / 2})`);
        
let color = d3.scaleOrdinal()
        .domain(familyData.map(function(d) { return d.family; }))
        .range(d3.schemeCategory10);

let pie = d3.pie()
        .value(function(d) { return d.total_millions; });

let arc = d3.arc()
        .innerRadius(0)
        .outerRadius(radius);

pieFrame.selectAll("path")
        .data(pie(familyData))
        .enter()
        .append("path")
        .attr("d", arc)
        .attr("fill", d => color(d.data.family))
        .attr("stroke", "white")
        .attr("stroke-width", "2px");

pieFrame.append("text")
        .attr("x", width2 / 2)
        .attr("y", margin / 2)
        .attr("transform", `translate(${-width2 / 2}, ${-height2 / 2})`)
        .attr("text-anchor", "middle")
        .style("font-size", "16px")
        .text("Most Spoken Language Families in the World as Share of Total");

pieFrame.selectAll("path")
        .on("click", function(event, d) {
          let total = d3.sum(familyData, d => d.total_millions);
          let percentage = ((d.data.total_millions / total) * 100);
          if (selected === d) {
            pieFrame.selectAll(".label").remove();
            selected = null;
          } 
          else {
            pieFrame.selectAll(".label").remove();
            d3.select(this)
            pieFrame.append("text")
            .attr("class", "label")
            .attr("x", 0)
            .attr("y", 0)
            .attr("text-anchor", "middle")
            .style("font-size", "12px")
            .text(`${d.data.family}: ${percentage.toFixed(1)}% of total`);
            selected = d;
          }
        }); 
